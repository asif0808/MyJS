// String()
//Number to String
let mark=22
let smark=String(mark)
console.log("Number 22 converts to ",smark,typeof(smark))
// boolean to String (same for true and false)
let A= false
let StrA=String(A)
console.log(A,typeof(A),"converts to ",StrA,typeof(StrA))
// null to String
let val=null
let Sval=String(null)
console.log(val,typeof val,"converts to ",Sval,typeof Sval)
// undefined to String
let Newv;
let sNewv=String(Newv)
console.log(Newv,typeof Newv,"converts to",sNewv,typeof sNewv)